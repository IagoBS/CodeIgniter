            Atualmente, a sede do GEEC funciona na Av. 21 de Abril, 1590, bairro Santa Clara. A construção da sede própria, no entanto, já está sendo preparada e será a realização de um grande sonho, pois permitirá ampliar e melhorar ainda mais a oferta dos serviços à comunidade.

